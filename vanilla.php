			<h2>Vanilla:</h2>
				<p>
                                    Vanilla is a flavoring derived from orchids of the genus Vanilla, primarily from the Mexican species, flat-leaved vanilla (V. planifolia). The word vanilla, derived from the diminutive of the Spanish word vaina (vaina itself meaning sheath or pod), is translated simply as "little pod". Pre-Columbian Mesoamerican people cultivated the vine of the vanilla orchid, called tlilxochitl by the Aztecs. Spanish conquistador Hernán Cortés is credited with introducing both vanilla and chocolate to Europe in the 1520s.<br/>
                                    Initial attempts to cultivate vanilla outside Mexico and Central America proved futile because of the symbiotic relationship between the vanilla orchid and its natural pollinator, the local species of Melipona bee. Pollination is required to set the fruit from which the flavoring is derived. In 1837, Belgian botanist Charles François Antoine Morren discovered this fact and pioneered a method of artificially pollinating the plant. The method proved financially unworkable and was not deployed commercially. In 1841, Edmond Albius, a slave who lived on the French island of Réunion in the Indian Ocean, discovered at the age of 12 that the plant could be hand-pollinated. Hand-pollination allowed global cultivation of the plant.<br/>
                                    Three major species of vanilla currently are grown globally, all of which derive from a species originally found in Mesoamerica, including parts of modern-day Mexico. They are V. planifolia (syn. V. fragrans), grown on Madagascar, Réunion, and other tropical areas along the Indian Ocean; V. tahitensis, grown in the South Pacific; and V. pompona, found in the West Indies, and Central and South America. The majority of the world's vanilla is the V. planifolia species, more commonly known as Bourbon vanilla (after the former name of Réunion, Île Bourbon) or Madagascar vanilla, which is produced in Madagascar and neighboring islands in the southwestern Indian Ocean, and in Indonesia.<br>
                                    Vanilla is the second-most expensive spice after saffron, because growing the vanilla seed pods is labor-intensive. Despite the expense, vanilla is highly valued for its flavor. As a result, vanilla is widely used in both commercial and domestic baking, perfume manufacture, and aromatherapy.<br/>
